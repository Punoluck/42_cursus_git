#ifndef FT_ISALPHA_H
# define FT_ISALPHA_H

int	ft_isalpha(int c)
{
	if ((('a' <= c) && (c <= 'z')) || (('A' <= c) && (c <= 'Z')))
	{
		return (1);
	}
	return (0);
}

#endif